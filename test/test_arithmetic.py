# Test arithmetic operations
print("=== Arithmetic Tests ===")

# Basic operations
a = 10
b = 5
print("a =", a)
print("b =", b)

print("a + b =", a + b)
print("a - b =", a - b)
print("a * b =", a * b)
print("a / b =", a / b)

# Float arithmetic
x = 3.5
y = 2.0
print("x =", x)
print("y =", y)
print("x + y =", x + y)
print("x * y =", x * y)

# Mixed int/float
print("a + x =", a + x)
print("b =", b)
print("y =", y)
print("b * y =", b * y)

# Expressions
result = (a + b) * 2
print("(a + b) * 2 =", result)

result = a + b * 2
print("a + b * 2 =", result)
